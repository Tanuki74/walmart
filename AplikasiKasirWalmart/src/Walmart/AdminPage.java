    private void btn_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SimpanActionPerformed
        // TODO add your handling code here:
        try {
        Connection conn = koneksi.koneksiDB();

        // Check if the record with the given code already exists
        String checkSql = "SELECT * FROM `barang` WHERE `kode_barang` = ?";
        try (PreparedStatement checkStatement = conn.prepareStatement(checkSql)) {
            checkStatement.setString(1, txt_Kode.getText());
            ResultSet resultSet = checkStatement.executeQuery();

            if (resultSet.next()) {
                // If record exists, perform an update
                String updateSql = "UPDATE `barang` SET `nama_barang`=?, `stock_barang`=?, `harga_barang`=?, `kategori_barang`=? WHERE `kode_barang`=?";
                try (PreparedStatement updateStatement = conn.prepareStatement(updateSql)) {
                    updateStatement.setString(1, txt_NamaB.getText());
                    updateStatement.setString(2, txt_Jumlah.getText());
                    updateStatement.setString(3, txt_Harga.getText());
                    updateStatement.setString(4, txt_Kategori.getText());
                    updateStatement.setString(5, txt_Kode.getText());
                    updateStatement.executeUpdate();
                }
                JOptionPane.showMessageDialog(rootPane, "Barang berhasil diupdate!");
            } else {
                // If record does not exist, perform an insert
                String insertSql = "INSERT INTO `barang` (`kode_barang`, `nama_barang`, `stock_barang`, `harga_barang`, `kategori_barang`) VALUES (?, ?, ?, ?, ?)";
                try (PreparedStatement insertStatement = conn.prepareStatement(insertSql)) {
                    insertStatement.setString(1, txt_Kode.getText());
                    insertStatement.setString(2, txt_NamaB.getText());
                    insertStatement.setString(3, txt_Jumlah.getText());
                    insertStatement.setString(4, txt_Harga.getText());
                    insertStatement.setString(5, txt_Kategori.getText());
                    insertStatement.executeUpdate();
                }
                JOptionPane.showMessageDialog(rootPane, "Barang berhasil ditambahkan!");
            }
        }
        txt_Kode.setText("");
        txt_NamaB.setText("");
        txt_Jumlah.setText("");
        txt_Harga.setText("");
        txt_Kategori.setText("");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_btn_SimpanActionPerformed


    private void txt_KodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_KodeActionPerformed
        // TODO add your handling code here:
        if(txt_Kode.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Kode Barang Tidak Boleh Kosong!!!");
        }else{
            try{
                Connection conn = koneksi.koneksiDB();
                st = conn.createStatement();
                ResultSet rs = st.executeQuery("Select nama_barang, stock_barang, harga_barang, kategori_barang From barang Where kode_barang Like '%" +txt_Kode.getText()+ "%'");
                if(rs.next()){
                    txt_NamaB.setText(rs.getString("nama_barang"));
                    txt_Jumlah.setText(rs.getString("stock_barang"));
                    txt_Harga.setText(rs.getString("harga_barang"));
                    txt_Kategori.setText(rs.getString("kategori_barang"));
                }
            }catch(Exception e){
                System.out.println("Error");
            }
        }
    }//GEN-LAST:event_txt_KodeActionPerformed

